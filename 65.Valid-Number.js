/**
 * @param {string} s
 * @return {boolean}
 */
var isNumber = function(s) {
  // Negative regex
  if (
    s.match(
      /^[\+\-]\.?[0-9]+\s*$|^\s*[0-9]+\s*$|^\s*[0-9]+\.e[0-9]+\s*$|^\s*[0-9]+\.?[0-9]+$|^\s*[\+\-]\.?[0-9]+e[0-9]+\s*$|^\s*[\+\-]?[0-9]+\.?[0-9]+e?[0-9]+\s*$|^\s*[\+\-]?[0-9]+\.?[0-9]+e[\+\-]?[0-9]+\s*$|^s*[0-9]+\.?e[\+\-]?[0-9]+\s*$|^\s*[\+\-][0-9]+\.?e[0-9]+\s*$|^\s*[\+\-]?\.[0-9]+e[\+\-]?[0-9]+\s*$|^\s*[\+\-]?[0-9]*\.[0-9]+\s*$|^[0-9]+\.\s*$/
    )
  )
    return true;
  let regex = /[0-9]+[\+\-]+[0-9]+|.+[e\.]+.+[e\.]+|[\+\-]e[0-9]+|.+\s+.+|[0-9]+\s+.+|[0-9]+\.+\s+.+|[0-9]+\s+e|e[0-9]+\.|\s+\.$|[\.e]{2,}|^.*\.[0-9]+\.|[Ea-df-zA-DF-Z\=]|[-+\.]{2,}|^.*e+$|^[a-zA-Z]|^[\s\.]+$|^.\s+.|\.e.|^.*e\$|^.*e\.$|^\..+\.$|^\s?e.+$|^.*e\s+$|^.+[\+\-].*/;
  if (s.match(regex)) return false;
  return true;
};

